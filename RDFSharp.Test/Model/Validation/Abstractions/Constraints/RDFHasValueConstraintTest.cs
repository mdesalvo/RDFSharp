        [TestMethod]
        public void ShouldThrowExceptionOnCreatingHasValueResourceConstraint()
            => Assert.ThrowsException<RDFModelException>(() => new RDFHasValueConstraint(null as RDFResource));

        [TestMethod]
        public void ShouldExportHasValueResourceConstraint()
        {
            RDFHasValueConstraint hasValueConstraint = new RDFHasValueConstraint(new RDFResource("ex:value"));
            RDFGraph graph = hasValueConstraint.ToRDFGraph(new RDFNodeShape(new RDFResource("ex:NodeShape")));

            Assert.IsNotNull(graph);
            Assert.IsTrue(graph.TriplesCount == 1);
            Assert.IsTrue(graph.IndexedTriples.Any(t => t.Value.SubjectID.Equals(new RDFResource("ex:NodeShape").PatternMemberID)
                                                    && t.Value.PredicateID.Equals(RDFVocabulary.SHACL.HAS_VALUE.PatternMemberID)
                                                        && t.Value.ObjectID.Equals(new RDFResource("ex:value").PatternMemberID)));
        }

