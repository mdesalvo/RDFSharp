        public void ShouldCreateHasValueResourceConstraint()
        {
            RDFHasValueConstraint hasValueConstraint = new RDFHasValueConstraint(new RDFResource("ex:value"));

            Assert.IsNotNull(hasValueConstraint);
            Assert.IsTrue(hasValueConstraint.Value.Equals(new RDFResource("ex:value")));
